`timescale 1ns/1ps

module tb_debounce;

    //------------------------------------------------
    // Parameters
    //------------------------------------------------

    parameter COUNT_MAX = 4;

    //------------------------------------------------
    // Signals
    //------------------------------------------------

    reg clk;
    reg rst;

    reg noisy;

    wire clean;

    //------------------------------------------------
    // DUT
    //------------------------------------------------

    debounce #(

        .COUNT_MAX(COUNT_MAX)

    ) uut (

        .clk(clk),
        .rst(rst),

        .noisy(noisy),

        .clean(clean)

    );

    //------------------------------------------------
    // Clock
    //------------------------------------------------

    always #5 clk = ~clk;

    //------------------------------------------------
    // Test
    //------------------------------------------------

    initial begin

        $dumpfile("debounce.vcd");
        $dumpvars(0,tb_debounce);

        clk = 0;
        rst = 1;

        noisy = 0;

        //------------------------------
        // Reset
        //------------------------------

        #20;

        rst = 0;

        //------------------------------
        // Simulated button bounce
        //------------------------------

        #10 noisy = 1;
        #10 noisy = 0;
        #10 noisy = 1;
        #10 noisy = 0;
        #10 noisy = 1;

        //------------------------------
        // Hold high
        //------------------------------

        #100;

        //------------------------------
        // Release with bounce
        //------------------------------

        noisy = 0;
        #10 noisy = 1;
        #10 noisy = 0;
        #10 noisy = 1;
        #10 noisy = 0;

        //------------------------------
        // Hold low
        //------------------------------

        #100;

        $finish;

    end

endmodule